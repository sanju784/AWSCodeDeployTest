    <p class="mb30">リラックスして検査を受けてください。<br>検尿等がある場合は、忘れずにお願いいたします。<br>当日の飲食の制限については医療機関の指示に従ってください。<br>当日は検査服に着替えることが多いので、着脱しやすい服をご用意ください。また婦人科検診の場合は、ジーンズやパンツをはいていくよりスカートをはいて行くことをおすすめします。またアクセサリーも外す事が必須となるため、なるべくひかえるようにしましょう。</p>
