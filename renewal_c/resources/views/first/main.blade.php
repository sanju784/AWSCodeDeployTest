    <h3 class="sTitle">人間ドックや各種健診の受診に向けて</h3>
    <div class="headline">前日の過ごし方</div>
    <p class="mb30">医療機関から飲食の制限等がある場合は指示に従ってください。<br>睡眠は十分にとり、お酒・タバコ類はおひかえください。<br>問診表、自己検査キットなどを渡されている場合は、事前にご準備をお願いいたします。</p>
