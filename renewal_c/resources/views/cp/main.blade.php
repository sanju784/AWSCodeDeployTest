		<h2>Q&amp;A</h2>

		<dl class="qa">
			<dt>電話受付でも本制度を利用できますか？</dt>
			<dd>誠に恐れ入れますが、お電話での受付は対象外です。	</dd>
		</dl>

		<dl class="qa">
			<dt>家族も本補助を利用できますか？</dt>
			<dd>ご家族の方も本補助の利用は可能です。<br>ただし、キャシュポが補助されるのはネット受付をされたEPARK会員様になります。</dd>
		</dl>

		<dl class="qa">
			<dt>ネット受付をしようとしたが、日付の選択ができません。</dt>
			<dd>誠に恐れ入りますが、医療機関様の諸事情により受付できません。その他の施設のご検討お願いいたします。</dd>
		</dl>

		<dl class="qa">
			<dt>ネット受付をキャンセルをした場合、再度、本制度は利用できますか？</dt>
			<dd>ネット受付のキャンセル後、改めてネット受付いただければ、ご利用可能です。</dd>
		</dl>

		<dl class="qa">
			<dt>領収証にもらい忘れ・不備がある場合は、どうすればいいですか？</dt>
			<dd>誠に恐れ入りますが、領収書がない場合は、キャシュポ補助ができかねます。</dd>
		</dl>

		<dl class="qa">
			<dt>キャシュポとは何ですか？</dt>
			<dd>キャシュポとは、商品のご購入や施設のご利用によって、現金が貯まるEPARKのサービスです。貯まったキャシュポは、ご登録した銀行口座へキャッシュバックができます。EPARKの一部サイトでお支払い時にご利用いただくことも可能です。<br>キャシュポについての詳細は<a href="https://cb.epark.jp/about?_ga=2.211322902.2027954756.1546823160-1872671559.1539831052" target="_blank">こちら</a></dd>
		</dl>

		<dl class="qa">
			<dt>キャシュポが補助されるタイミングを教えてください。</dt>
			<dd>キャシュポの付与の時期は、申請完了した月の翌月末日の予定になります。</dd>
		</dl>

		<dl class="qa">
			<dt>5,000円未満のメニューを受けた場合でも、一律5,000円分のキャシュポが補助されますか？</dt>
			<dd>申し訳ございません。5,000円未満のメニューを受けた際の補助されるキャシュポは、実際に受けた金額が上限となります。</dd>
